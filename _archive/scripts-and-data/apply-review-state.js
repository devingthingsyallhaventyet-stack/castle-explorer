let flaggedCount = 0;
let approvedCount = 0;

data.forEach(d => {
  const key = d.name + '|' + d.country;
  
  if (flaggedWithReasons[key]) {
    d.reviewStatus = 'flagged';
    d.reviewReasons = flaggedWithReasons[key].reasons;
    if (flaggedWithReasons[key].note) d.reviewNote = flaggedWithReasons[key].note;
    flaggedCount++;
  } else if (needsReview.includes(key)) {
    d.reviewStatus = 'flagged';
    d.reviewReasons = ['needs-review'];
    flaggedCount++;
  } else {
    d.reviewStatus = 'approved';
    approvedCount++;
  }
});

